package pack1;

public class Operatorsdemo
{

	public static void main(String[]args)
	{
		
		int x=10;
		 
		int y= ++x; // first value of X will be increased by 1 and then it will be assigned to Y
		
		System.out.println(y);
	
		int p=10;
		int q= p++; // first value of P will assigned to q and then increased by 1
		
		System.out.println("Value of q:" +q);
		System.out.println("Value of p:" +p);
		
		
	}
}
